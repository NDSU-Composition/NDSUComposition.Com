		<p>Hailing from Duluth, Minnesota, Jonathon is an undergraduate at North Dakota State University following a Bachelor of Science in Music as well as Public History. Jonathon got his start in music at a young age by taking piano lessons, a path inspired by his time sitting with his Grandmother behind the organ at church and their mutual bonding over the art. Later in life, Jonathon began playing clarinet for his high schools band. After a short break and returning to school, Jonathon learned that he had a hidden love for composition, in which he cites his largest influences as Koji Kondo and John Williams.</p>
