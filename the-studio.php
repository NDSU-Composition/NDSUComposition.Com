		<h3>Dr. Kyle Vanderburg</h3>
		<p>Composer Kyle Vanderburg (b. 1986) grew up in southeast Missouri where the Ozark foothills meet the Mississippi River valley. Raised on southern gospel and American hymnody, his music tries to walk the line between eliciting nostalgia and devising innovative sonic worlds.  His electronic works often play with familiar sounds in new contexts; his acoustic works feature memorable melodies and a very fluid sense of time.</p>
		<p>Vanderburg's music has been heard abroad at conferences and festivals including the International Computer Music Conference, the Society for Electro-Acoustic Music in the United States, the Symposium on Acoustic Ecology, the North American Saxophone Alliance, ClarinetFest, and at other events across Europe, Australia, and the Americas.</p>
		<p>He holds an AB from Drury University, an MM and DMA from the University of Oklahoma, and a certificate in publishing from North Dakota State University, studying under composers Carlyle Sharpe, Marvin Lamb, Konstantinos Karathanasis, and Roland Barrett. He is currently Assistant Professor of Practice and Composer in Residence at North Dakota State University.</p><hr><h2>

