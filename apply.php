<h1>Apply</h1>
	<div class="alert alert-info">Applying for any music degree at NDSU is a multiple-stage process. In composition, students must apply to <a href="">the University</a>, the <a href="">Challey School of Music<a>, and to the Composition Program.</div>
	
	<h4>I just want to take composition lessons</h4>
	<p>Music students who want to pursue composition lessons as enrichment may do so if allowed by the instructor and if they have completed the prerequsites.</p>

	<hr>

	<h4>Applying to the Composition Major</h4>
	<p>Students who want to pursue composition as a major must:
		<ol>
