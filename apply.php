<div class="alert alert-info">
<h2>The Composition Portfolio</h2>
Your composition portfolio should include the following items:
<ul>
	<li>Three to five composed works, preferably notated in a standard notation program (Sibelius, Dorico, Finale, Musescore, etc.) or equivalent. 
		<ul><li>If your works are recorded but not notated, please submit recordings.</li></ul>
	</li>
	<li>Recordings of your scores (if available)</li>
	<li>a brief document describing<ul>
		<li>your past experience with composing</li>
		<li>a list of past performances of compositions</li>
		<li>career goals while studying composition at NDSU</li>
	</ul>
	<li>For current NDSU Students: A letter of intention to change your major.</li>
</ul>

The portfolio may be submitted (choose one):

<ul>
<li>by mail to Kyle Vanderburg, PO Box 6050, Dept. 2540, Fargo, ND 58108</li>
<li>by PDF to kyle.vanderburg@ndsu.edu</li>
<li>by uploading files to <a href="/upload/">ndsucomposition.com/upload</a></li>
</ul>
</div>