				<div class="resume-section-content">
					<h2 class="mb-5">Apply</h2>
						<p>Applying for any music degree at NDSU is a multiple-stage process. In composition, students must apply to the University, the School of Music, and to the Program.</p>
						
						<h4>Taking Composition Lessons</h4>
						<p>Music students who want to pursue composition lessons as enrichment may do so if allowed by the instructor and if they have completed the prerequsites.</p>

						<h4>Majoring in Composition</h4>
						<p>Students who want to pursue composition as a major must complete a performance audition (in voice or on a standard band/orchestral instrument) for acceptance into the Challey School of Music before their first semester as a music major. To be considered a composition major, prospective students must also submit a portfolio of works for evaluation. </p>
						
						<p>Students may apply to the composition program at the same time they apply to the Challey School of Music, or students may apply to the composition program at any point after being admitted to the Challey School of Music.</p>
						
						<p>Up-to-date information on applying to the Challey School of Music is available at <a href="//ndsu.edu/music/prospective-students/">https://ndsu.edu/music/prospective-students/</a>.</p>

